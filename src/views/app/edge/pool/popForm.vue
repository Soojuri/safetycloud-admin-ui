<template>
  <div>
    <el-dialog :visible="visible" width="888px" :title="title" append-to-body :close-on-click-modal='false'
               @close="handleClose">
      <el-row>
        <el-col :offset="3" :span="16">
          <el-form :rules="rules" :model='form' label-width='120px' ref='form'>
            <el-form-item label='运行池ID' prop='edgePoolId'>
              <el-input size='small' :disabled="id" v-model='form.edgePoolId' placeholder='请输入运行池ID'></el-input>
            </el-form-item>
            <el-form-item label='运行池名称' prop='poolName'>
              <el-input size='small' v-model='form.poolName' placeholder='请输入运行池名称'></el-input>
            </el-form-item>
            <el-form-item label='描述' prop='remark'>
              <el-input size='small' v-model='form.remark' placeholder='请输入描述' maxlength="255" type="textarea"
                        :rows="5"></el-input>
            </el-form-item>
          </el-form>
        </el-col>
      </el-row>
      <div slot='footer' class="dialog-footer">
        <el-button @click="handleClose">取 消</el-button>
        <el-button type="primary" @click="handleSave">保 存</el-button>
      </div>
    </el-dialog>
  </div>
</template>

<script>
import { getPoolInfo, putPool, addPool } from '@/api/app/edge/pool'
export default {
  components: {},
  props: {
    visible: {
      type: Boolean,
      default: false,
    },
    data: {
      type: Object,
      default: () => {
        return {}
      },
    },
    dict: {
      type: Object,
      default: () => {
        return {}
      },
    },
  },
  data() {
    return {
      form: {},
      id: null,
      rules: {
        edgePoolId: [this.$formRules.checkLen(50), this.$formRules.checkNecessary('请输入运行池ID')],
        poolName: [this.$formRules.checkLen(255), this.$formRules.checkNecessary('请输入运行池名称')],
      },
    }
  },
  computed: {
    title() {
      return this.id ? '编辑' : '新增'
    },
  },
  watch: {},
  created() {
    this.id = this.data ? this.data.id : null
  },
  mounted() {
    this.getList()
  },
  methods: {
    getList() {
      if (this.id) {
        getPoolInfo(this.id).then((res) => {
          this.form = res.data.data
        })
      }
    },
    handleClose() {
      this.resetForm('form')
      this.$emit('update:visible', false)
      this.$emit('ok')
    },
    handleSave() {
      const that = this
      this.$refs.form.validate((valid) => {
        if (valid) {
          if (that.id) {
            putPool(this.form).then((res) => {
              if (res.data.data) {
                that.handleClose()
                that.msgSuccess('修改成功')
              }
            })
          } else {
            addPool(this.form).then((res) => {
              if (res.data.data) {
                that.handleClose()
                that.msgSuccess('新增成功')
              }
            })
          }
        }
      })
    },
  },
}
</script>
<style lang='scss' scoped>
::v-deep .dialog-footer {
  text-align: center;
}
